package com.zhangjie.logowall.model;

public class WallInfo {
	
	private Long id;
	private Long userId;
	private Long logoId;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getUserId() {
		return userId;
	}
	public void setUserId(Long userId) {
		this.userId = userId;
	}
	public Long getLogoId() {
		return logoId;
	}
	public void setLogoId(Long logoId) {
		this.logoId = logoId;
	}
	
}
